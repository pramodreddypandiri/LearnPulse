// This file is the SINGLE SOURCE OF TRUTH for all TypeScript types
// used across the LearnPulse pipeline. Every component, hook, API route,
// and AI module imports from here.
//
// PIPELINE OVERVIEW:
//   Raw Input → HistoryEntry[] → ClassifiedEntry[] → LearningCluster[] → GeneratedPosts
//
// HOW TYPES FLOW:
//   1. Parsers produce          : HistoryEntry[]
//   2. /api/classify produces   : ClassifiedEntry[]   (extends HistoryEntry)
//   3. /api/cluster produces    : LearningCluster[]   (contains ClassifiedEntry[])
//   4. /api/generate produces   : GeneratedPosts      (based on LearningCluster[])
